    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int contador = 0;
        if(App_SCajaChica.tablaD.getRowCount()<=0){
            contador = 1;
        }else{
            contador = Integer.parseInt(App_SCajaChica.tablaD.getValueAt(App_SCajaChica.tablaD.getRowCount()-1, 0).toString())+1;
        }
       App_SCajaChica.modelo.addRow(new Object[]{contador,desc.getText(),cboUM.getSelectedItem().toString(),cant.getText(),uso.getText(),pu.getText(),importe.getText()});
       double total = 0.0;
       for (int i = 0; i <App_SCajaChica.tablaD.getRowCount();  i++) {
            total = total + Double.parseDouble(App_SCajaChica.tablaD.getValueAt(i, 6).toString());
            
        }
       App_SCajaChica.importe.setText(String.valueOf(total));
    }//GEN-LAST:event_jButton1ActionPerformed


