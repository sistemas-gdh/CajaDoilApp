    public void updateT(long id,int valor) {
        if (valor == 1) {
            DefaultTableModel mod = (DefaultTableModel) App_LiquidarCC.tablalcc.getModel();
            oper.limpiaTabla(mod, App_LiquidarCC.tablalcc);
            mod = oDMCS.obtenerDMCS((DefaultTableModel) App_LiquidarCC.tablalcc.getModel(), 0, id);
            App_LiquidarCC.tablalcc.setModel(mod);
        } else {
            DefaultTableModel mod = (DefaultTableModel) App_LiquidarCC.tablalcc.getModel();
            oper.limpiaTabla(mod, App_LiquidarCC.tablalcc);
            mod = oDMCD.obtenerDMCD((DefaultTableModel) App_LiquidarCC.tablalcc.getModel(), 0, id);
            App_LiquidarCC.tablalcc.setModel(mod);
        }

    }
    
    public void updateTSD(int valor) {
        if(valor == 1){
            DefaultTableModel mod = (DefaultTableModel) App_PreLiqCaja.tablals.getModel();
            oper.limpiaTabla(mod, App_PreLiqCaja.tablals);
            mod = oMCS.getMcsByUser(mod, Integer.parseInt(App_Principal.codigo.getText()),0,1);
            App_PreLiqCaja.tablals.setModel(mod);
        }else{
            DefaultTableModel mod = (DefaultTableModel) App_PreLiqCaja.tablald.getModel();
            oper.limpiaTabla(mod, App_PreLiqCaja.tablald);
            mod = oMCD.getMcdByUser(mod, Integer.parseInt(App_Principal.codigo.getText()),0);
            App_PreLiqCaja.tablald.setModel(mod);  
        }

    }


