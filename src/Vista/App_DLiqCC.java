    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(App_LiquidarCC.bandera==1){
            DetalleMcs detm = oDMCS.buscaDMCS(codDetLiq);
            oDMCS.aDMCS(detm.getDmcsId(), umet.getText(), desc.getText(),Double.parseDouble(cant.getText()),
            Double.parseDouble(importe.getText()), Double.parseDouble(sub.getText()), 
            cboTD.getSelectedItem().toString(), prov.getText(), serie.getText(), fechar.getDate(), Double.parseDouble(totald.getText()));
            MovimientosCajas tempmcs = oMCS.buscaMCS(detm.getMovimientosCajas().getMcsId());
            oMCS.aILiquidado(detm.getMovimientosCajas().getMcsId(), tempmcs.getMcsImportel()+Double.parseDouble(sub.getText()));
            updateT(detm.getMovimientosCajas().getMcsId(),1);
            int pl = oDMCS.searchLiqS(detm.getMovimientosCajas().getMcsId(),0);
            if(pl > 0){
                this.dispose();
            }else{
                //actualizar estado MCS a SUBIDO
                oMCS.aEstado(detm.getMovimientosCajas().getMcsId(), 1);
                App_LiquidarCC.btnS.doClick();
                updateTSD(1);
                this.dispose();
            }
            
            
        }else{
            DetalleMcd detmd = oDMCD.buscaDMCD(codDetLiq);
            
            oDMCD.aDMCD(detmd.getDmcdId(), umet.getText(), desc.getText(), Double.parseDouble(cant.getText()),
            Double.parseDouble(importe.getText()), Double.parseDouble(sub.getText()),
            cboTD.getSelectedItem().toString(), prov.getText(), serie.getText(), fechar.getDate(), Double.parseDouble(totald.getText()));
            MovimientosCajasd tempmcd = oMCD.buscaMCD(detmd.getMovimientosCajasd().getMcdId());
            oMCD.aILiquidado(detmd.getMovimientosCajasd().getMcdId(), tempmcd.getMcdImportel()+Double.parseDouble(sub.getText()));
            updateT(detmd.getMovimientosCajasd().getMcdId(),2);
            
            int pl = oDMCD.searchLiqD(detmd.getMovimientosCajasd().getMcdId(),0);
            if(pl > 0){ 
                this.dispose();
            }else{
                //actualizar estado MCD a SUBIDO
                oMCD.aEstado(detmd.getMovimientosCajasd().getMcdId(), 1);
                App_LiquidarCC.btnS.doClick();
                updateTSD(2);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


