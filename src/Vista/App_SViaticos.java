    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        


        Usuarios u = oU.buscaUsuarios(Integer.parseInt(App_Principal.codigo.getText()));
        SolicitudViaticos temppc = oSV.getLast();
        long corr;
        if(temppc == null){
            corr = 1;
        }else{
            corr = temppc.getSvId() + 1;
        }
        
        String serie = oper.generarSerie(corr,4);
        
        SolicitudViaticos pc = new SolicitudViaticos(u,serie,oper.getF(),desc.getText(),Double.parseDouble(imp.getText()),0,0,0,corr,0);
        oper.registrar(pc);
        

        
        SolicitudViaticos pdc = oSV.buscaSCC(oper.getNextID());

        Iterator<Object[]> itrPartidos = lista.iterator();
        while (itrPartidos.hasNext()) {
            Object[] dv = itrPartidos.next();
            
            Choferes cs = oCS.buscaCd(Integer.parseInt(dv[0].toString()));
            Remolques rq = oR.buscaR(Integer.parseInt(dv[1].toString()));
            ServTransporte stt = oST.buscaST(Integer.parseInt(dv[3].toString()));
            //SV
            Tractos tt = oT.buscaT(Integer.parseInt(dv[2].toString()));
            //dv[4],dv[5],dv[6],dv[7],dv[8],dv[9],dv[10]
            
            DetalleSv dsv = new DetalleSv(cs,rq,stt,pdc,tt,Double.parseDouble(dv[4].toString()),Double.parseDouble(dv[5].toString()),
                    Double.parseDouble(dv[6].toString()),Double.parseDouble(dv[7].toString()),
                    Double.parseDouble(dv[8].toString()),Double.parseDouble(dv[9].toString()),
                    Double.parseDouble(dv[10].toString()));
            
            oper.registrar(dsv);
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

